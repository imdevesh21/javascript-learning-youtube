const month = "march";

switch (month) {
    case "jan":
        console.log('January');
        break;
    case "feb":
        console.log('fabuary');
        break;
    case "march":
        console.log('march');
        break;
    case "april":
        console.log('april');
        break;
    default:
        console.log('invalid month');
        break;
}